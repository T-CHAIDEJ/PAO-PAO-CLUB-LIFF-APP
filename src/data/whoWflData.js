export function getWHOWflData(gender) {
  const wfl = gender === 'male' ? WFL_BOYS : WFL_GIRLS;
  const wfh = gender === 'male' ? WFH_BOYS : WFH_GIRLS;
  return [...wfl.filter(d => d.length < 65), ...wfh];
}

export function getWHOWflAtLength(gender, lengthCm) {
  const data = getWHOWflData(gender);
  const lo = [...data].reverse().find(d => d.length <= lengthCm);
  const hi = data.find(d => d.length > lengthCm);
  if (!lo) return hi || data[0];
  if (!hi) return lo;
  const t = (lengthCm - lo.length) / (hi.length - lo.length);
  return {
    sd2neg: +(lo.sd2neg + t * (hi.sd2neg - lo.sd2neg)).toFixed(2),
    median: +(lo.median + t * (hi.median - lo.median)).toFixed(2),
    sd2pos: +(lo.sd2pos + t * (hi.sd2pos - lo.sd2pos)).toFixed(2),
  };
}
